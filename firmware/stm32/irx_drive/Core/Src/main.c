// Parse and execute command
void parse_command(char* cmd)
{
    int left_rpm = 0, right_rpm = 0;
    if (sscanf(cmd, "CMD %d %d", &left_rpm, &right_rpm) == 2)
    {
        robot_set_rpm((int32_t)left_rpm, (int32_t)right_rpm);
        char response[32];
        snprintf(response, sizeof(response), "OK %d %d\n", left_rpm, right_rpm);
        HAL_UART_Transmit(&huart2, (uint8_t*)response, strlen(response), 100);
    }
    else
    {
        // Debug — echo what was received
        char debug[32];
        snprintf(debug, sizeof(debug), "ERR:%s\n", cmd);
        HAL_UART_Transmit(&huart2, (uint8_t*)debug, strlen(debug), 100);
    }
}

int main(void)
{
    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();
    MX_CAN1_Init();
    MX_USART2_UART_Init();

    // 3 second debug window
    HAL_Delay(3000);

    // CAN Filter
    CAN_FilterTypeDef filter;
    filter.FilterBank = 0;
    filter.FilterMode = CAN_FILTERMODE_IDMASK;
    filter.FilterScale = CAN_FILTERSCALE_32BIT;
    filter.FilterIdHigh = 0x0000;
    filter.FilterIdLow = 0x0000;
    filter.FilterMaskIdHigh = 0x0000;
    filter.FilterMaskIdLow = 0x0000;
    filter.FilterFIFOAssignment = CAN_RX_FIFO0;
    filter.FilterActivation = ENABLE;

    if (HAL_CAN_ConfigFilter(&hcan1, &filter) != HAL_OK) Error_Handler();
    if (HAL_CAN_Start(&hcan1) != HAL_OK) Error_Handler();

    uint32_t last_blink = 0;
    uint32_t last_cmd_time = 0;
    char rx_buf[32];
    uint8_t rx_idx = 0;
    uint8_t rx_byte;

    while (1)
    {
        uint32_t now = HAL_GetTick();

        // Blink every 500ms
        if (now - last_blink >= 500)
        {
            last_blink = now;
            HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
        }

        // Safety watchdog — stop motors if no command for 1 second
        if (now - last_cmd_time >= 1000)
        {
            robot_set_rpm(0, 0);
        }

        // Replace the UART receive section in while(1) with:
        if (HAL_UART_Receive(&huart2, &rx_byte, 1, 5) == HAL_OK)
        {
            if (rx_byte == '\n')
            {
                rx_buf[rx_idx] = '\0';
                parse_command(rx_buf);
                last_cmd_time = now;
                rx_idx = 0;
            }
            else if (rx_idx < sizeof(rx_buf) - 1)
            {
                rx_buf[rx_idx++] = rx_byte;
            }
        }
    }
}

